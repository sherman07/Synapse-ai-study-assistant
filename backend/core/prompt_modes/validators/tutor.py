from .common import SOURCE_STRICT_ARTIFACT_HEADINGS, validate_mode_shape


def validate_tutor(summary: str, context: dict) -> str:
    return validate_mode_shape(
        summary,
        context,
        mode_label="Tutor Mode",
        expected_headings=(
            "Start From The Basic Idea",
            "Build The Concept Step By Step",
            "Where Students Usually Get Confused",
            "Worked Example / Guided Explanation",
            "Try This",
            "Check Your Understanding",
        ),
        forbidden_headings=(
            *SOURCE_STRICT_ARTIFACT_HEADINGS,
            "Direct Answer",
            "Source Evidence",
            "Do / Avoid",
            "Need-to-Know Formula / Example",
            "Learning Question",
            "Core Explanation",
            "How the Reasoning Works",
            "Worked Examples and Evidence",
            "Concept Comparison",
            "Revision Checklist",
            "Main Idea",
            "Key Concepts",
            "Step-by-Step Explanation",
            "Examples / Diagrams / Formulas",
            "Practice / Revision Checklist",
            "Big Picture",
            "What You Actually Need To Understand",
            "Concept Connections",
            "Deep Explanation",
            "Background Knowledge Layer",
            "Application To New Situations",
            "High-Quality Student Thinking",
            "How To Use This In Assessment",
            "Model High-Quality Output",
            "Memory and Practice",
            "Working Thesis / Answer",
            "APA-Style Outline",
            "Evidence Paragraphs",
            "References From Uploaded Sources",
        ),
        forbidden_phrases=(
            "Professional Mode",
            "Source-Strict Research Mode",
            "Assignment / APA Mode",
            "Quick Answer",
            "Detailed Explanation",
        ),
    )
